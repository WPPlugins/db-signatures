<h2>How to create an HTML signature?</h2>
<ol>
	<li>Go to DB Signatures > Add New</li>
	<li>Add a title for the signature, for example, <i>New Book</i></li>
	<li>On the content area add whatever content you want. It could be just some text, an image, a video.</li>
	<li>On the right site, choose in what post types that signature is allowed to appear (posts, pages, ...)</li>
	<li>You can also hide the signature on certain categories or tags</li>
</ol>

<h2>How to hide the signatures on a certain post?</h2>
<ol>
	<li>Open the post you want to disable the signatures</li>
	<li>On the right side, you should have a metabox "<strong>Choose Signature</strong>"</li>
	<li>Choose the option: X Disabled X</li>
</ol>

<h2>How to show always the same signature on a certain post?</h2>
<ol>
	<li>Open the post you want to disable the signatures</li>
	<li>On the right side, you should have a metabox "<strong>Choose Signature</strong>"</li>
	<li>Choose the signature you want to force to show</li>
</ol>